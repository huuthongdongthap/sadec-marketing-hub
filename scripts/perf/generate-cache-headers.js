#!/usr/bin/env node
/**
 * Sa Đéc Marketing Hub - HTTP Cache Headers Generator
 * Tạo .htaccess và cấu hình cache control headers
 *
 * Usage: node scripts/perf/generate-cache-headers.js
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

// Fix: Use proper ES module path resolution
const __filename = fileURLToPath(import.meta.url);
const ROOT_DIR = path.resolve(__filename, '../../../');
const ADMIN_DIR = path.join(ROOT_DIR, 'admin');
const PORTAL_DIR = path.join(ROOT_DIR, 'portal');
const AFFILIATE_DIR = path.join(ROOT_DIR, 'affiliate');
const AUTH_DIR = path.join(ROOT_DIR, 'auth');

/**
 * Tạo .htaccess với cache headers tối ưu
 */
const htaccessContent = `# Sa Đéc Marketing Hub - Performance Cache Configuration
# Generated by scripts/perf/generate-cache-headers.js

# Enable Gzip Compression
<IfModule mod_deflate.c>
    # Text files
    AddOutputFilterByType DEFLATE text/html
    AddOutputFilterByType DEFLATE text/plain
    AddOutputFilterByType DEFLATE text/xml
    AddOutputFilterByType DEFLATE text/css
    AddOutputFilterByType DEFLATE text/javascript
    AddOutputFilterByType DEFLATE application/javascript
    AddOutputFilterByType DEFLATE application/x-javascript
    AddOutputFilterByType DEFLATE application/json
    AddOutputFilterByType DEFLATE application/xml
    AddOutputFilterByType DEFLATE application/xhtml+xml
    AddOutputFilterByType DEFLATE image/svg+xml

    # Font files
    AddOutputFilterByType DEFLATE font/otf
    AddOutputFilterByType DEFLATE font/ttf
    AddOutputFilterByType DEFLATE font/woff
    AddOutputFilterByType DEFLATE font/woff2

    # Remove browser quirks
    BrowserMatch ^Mozilla/4 gzip-only-text/html
    BrowserMatch ^Mozilla/4\\.0[678] no-gzip
    BrowserMatch \\bMSIE !no-gzip !gzip-only-text/html
</IfModule>

# Enable Browser Caching
<IfModule mod_expires.c>
    ExpiresActive On

    # Images - 1 year
    ExpiresByType image/jpeg "access plus 1 year"
    ExpiresByType image/jpg "access plus 1 year"
    ExpiresByType image/png "access plus 1 year"
    ExpiresByType image/gif "access plus 1 year"
    ExpiresByType image/webp "access plus 1 year"
    ExpiresByType image/svg+xml "access plus 1 year"
    ExpiresByType image/x-icon "access plus 1 year"
    ExpiresByType image/vnd.microsoft.icon "access plus 1 year"

    # Fonts - 1 year
    ExpiresByType font/otf "access plus 1 year"
    ExpiresByType font/ttf "access plus 1 year"
    ExpiresByType font/woff "access plus 1 year"
    ExpiresByType font/woff2 "access plus 1 year"
    ExpiresByType application/font-woff "access plus 1 year"
    ExpiresByType application/font-woff2 "access plus 1 year"

    # CSS - 1 month (or use fingerprinted versions for 1 year)
    ExpiresByType text/css "access plus 1 month"

    # JavaScript - 1 month (or use fingerprinted versions for 1 year)
    ExpiresByType application/javascript "access plus 1 month"
    ExpiresByType application/x-javascript "access plus 1 month"
    ExpiresByType text/javascript "access plus 1 month"

    # HTML - 1 hour (always fresh for content updates)
    ExpiresByType text/html "access plus 1 hour"

    # JSON - 1 hour
    ExpiresByType application/json "access plus 1 hour"

    # XML - 1 hour
    ExpiresByType application/xml "access plus 1 hour"
    ExpiresByType text/xml "access plus 1 hour"

    # PDF - 1 week
    ExpiresByType application/pdf "access plus 1 week"

    # Default - 1 week
    ExpiresDefault "access plus 1 week"
</IfModule>

# Cache-Control Headers (more granular control)
<IfModule mod_headers.c>
    # Cache Control for static assets - 1 year with immutable
    <FilesMatch "\\.(jpg|jpeg|png|gif|webp|svg|ico|pdf|mp4|webm|ogg|mp3|wav)$">
        Header set Cache-Control "public, max-age=31536000, immutable"
    </FilesMatch>

    # Cache Control for fonts - 1 year with immutable
    <FilesMatch "\\.(woff2?|ttf|otf|eot)$">
        Header set Cache-Control "public, max-age=31536000, immutable"
    </FilesMatch>

    # Cache Control for CSS - 1 month (use versioned files for longer)
    <FilesMatch "\\.css$">
        Header set Cache-Control "public, max-age=2592000"
    </FilesMatch>

    # Cache Control for JavaScript - 1 month (use versioned files for longer)
    <FilesMatch "\\.(js|mjs)$">
        Header set Cache-Control "public, max-age=2592000"
    </FilesMatch>

    # Cache Control for HTML - No cache, always validate
    <FilesMatch "\\.(html|htm|shtml)$">
        Header set Cache-Control "public, max-age=3600, must-revalidate"
    </FilesMatch>

    # Cache Control for JSON - 1 hour
    <FilesMatch "\\.json$">
        Header set Cache-Control "public, max-age=3600"
    </FilesMatch>

    # Prevent caching for dynamic content
    <FilesMatch "\\.(php|asp|aspx|jsp)$">
        Header set Cache-Control "no-cache, no-store, must-revalidate"
        Header set Pragma "no-cache"
        Header set Expires "0"
    </FilesMatch>

    # Security Headers
    Header set X-Content-Type-Options "nosniff"
    Header set X-Frame-Options "SAMEORIGIN"
    Header set X-XSS-Protection "1; mode=block"
    Header set Referrer-Policy "strict-origin-when-cross-origin"

    # Remove server signature
    Header unset Server
    Header unset X-Powered-By
</IfModule>

# Enable ETag for validation
<IfModule mod_headers.c>
    Header set ETag "W/\\"%{UNIQUE_ID}s\\""
</IfModule>

# Disable ETag if using file-based versioning
# Header unset ETag

# Vary header for compression
<IfModule mod_headers.c>
    Header append Vary Accept-Encoding
</IfModule>

# Prevent image hotlinking (optional)
<IfModule mod_rewrite.c>
    RewriteEngine on
    RewriteCond %{HTTP_REFERER} !^$
    RewriteCond %{HTTP_REFERER} !^https?://(www\\.)?sadecmarketinghub\\.com [NC]
    RewriteCond %{HTTP_REFERER} !^https?://(www\\.)?localhost [NC]
    RewriteRule \\.(jpg|jpeg|png|gif|webp|svg)$ - [F,L]
</IfModule>

# CORS for fonts (if needed)
<FilesMatch "\\.(woff2?|ttf|otf|eot)$">
    <IfModule mod_headers.c>
        Header set Access-Control-Allow-Origin "*"
    </IfModule>
</FilesMatch>

# UTF-8 encoding
AddDefaultCharset UTF-8
AddType text/html .html
AddType text/css .css
AddType application/javascript .js
AddType application/json .json
AddType image/svg+xml .svg

# Prevent MIME type sniffing
<IfModule mod_headers.c>
    Header set X-Content-Type-Options "nosniff"
</IfModule>
`;

/**
 * Tạo .htaccess cho thư mục chỉ định
 */
function createHtaccess(dirPath, filename = '.htaccess') {
    if (!fs.existsSync(dirPath)) {
        console.log(`   ⚠️  Thư mục không tồn tại: ${dirPath}`);
        return false;
    }

    const filePath = path.join(dirPath, filename);
    fs.writeFileSync(filePath, htaccessContent, 'utf8');
    console.log(`   ✓ Đã tạo ${filename} tại ${dirPath}`);
    return true;
}

/**
 * Main function
 */
function main() {
    console.log('🔧 Tạo HTTP Cache Headers Configuration...\n');

    const dirs = [
        { path: ROOT_DIR, name: 'Root' },
        { path: ADMIN_DIR, name: 'Admin' },
        { path: PORTAL_DIR, name: 'Portal' },
        { path: AFFILIATE_DIR, name: 'Affiliate' },
        { path: AUTH_DIR, name: 'Auth' }
    ];

    let created = 0;
    let skipped = 0;

    for (const dir of dirs) {
        if (createHtaccess(dir.path)) {
            created++;
        } else {
            skipped++;
        }
    }

    console.log('\n' + '='.repeat(50));
    console.log('📊 Kết quả:');
    console.log(`   Đã tạo: ${created} files`);
    console.log(`   Bỏ qua: ${skipped} files`);
    console.log('='.repeat(50));
    console.log('\n✅ Cache headers configuration complete!\n');
    console.log('📝 Các headers đã được cấu hình:');
    console.log('   - Gzip/Deflate compression');
    console.log('   - Expires headers (1 year cho static assets)');
    console.log('   - Cache-Control headers (public/private, max-age)');
    console.log('   - Security headers (X-Content-Type-Options, X-Frame-Options)');
    console.log('   - CORS headers cho fonts');
    console.log('   - MIME type configuration');
    console.log('\n⚠️  Lưu ý: .htaccess chỉ hoạt động với Apache server.');
    console.log('   Đối với Nginx/Cloudflare, cần cấu hình tương tự trong server block.\n');
}

main();
